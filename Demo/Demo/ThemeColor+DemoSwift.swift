import Foundation
import ThemeKit

extension ThemeColor {
    
    /// Notes text foreground color
    static var contentTextColor: ThemeColor {
        return ThemeColor.color(with: #function)
    }
    
    /// Notes text background color
    static var contentBackgroundColor: ThemeColor {
        return ThemeColor.color(with: #function)
    }
    
}
